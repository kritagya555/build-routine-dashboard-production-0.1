// Helper function to get food by ID (simulates Prisma findUnique)
export function getFoodById(id: string): FoodItem | undefined {
  return indianFoodsData.find(food => food.id === id);
}

// Helper function to search foods (simulates Prisma findMany with search)
export function searchFoods(query: string): FoodItem[] {
  const lowerQuery = query.toLowerCase();
  return indianFoodsData.filter(food => 
    food.name.toLowerCase().includes(lowerQuery) ||
    (food.nameHindi && food.nameHindi.includes(query)) ||
    food.category.toLowerCase().includes(lowerQuery)
  );
}

// Helper function to get foods by category
export function getFoodsByCategory(category: FoodItem['category']): FoodItem[] {
  return indianFoodsData.filter(food => food.category === category);
}

// Calculate nutrition based on quantity (simulates server-side calculation)
export function calculateNutrition(foodId: string, quantityInGrams: number) {
  const food = getFoodById(foodId);
  if (!food) return null;
  
  const factor = quantityInGrams / 100;
  
  return {
    calories: Math.round(food.calories100g * factor * 10) / 10,
    protein: Math.round(food.protein100g * factor * 10) / 10,
    carbs: Math.round(food.carbs100g * factor * 10) / 10,
    fats: Math.round(food.fats100g * factor * 10) / 10,
    fiber: food.fiber100g ? Math.round(food.fiber100g * factor * 10) / 10 : 0
  };
}

// Get all categories
export function getAllCategories(): FoodItem['category'][] {
  return [...new Set(indianFoodsData.map(food => food.category))];
}
